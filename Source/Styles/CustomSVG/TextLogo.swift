import UIKit

class TextLogo {
    let content: String
    let font: UIFont
    let colGradMask: ColorGradientMask
    
    init(content: String, font: UIFont, colGradMask: ColorGradientMask) {
        self.content = content
        self.font = font
        self.colGradMask = colGradMask
    }
}
